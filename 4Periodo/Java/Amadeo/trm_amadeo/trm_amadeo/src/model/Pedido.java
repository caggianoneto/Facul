package model;


public class Pedido {
	
	private java.sql.Date dataEntrega;
	private java.sql.Date dataCadastro;
	private String status;
	private String justificativaStatus;
	private float comprimento;
	private float altura;
	private float largura;
	private float peso;
	private int prazo;
	private int idRemetente;
	private int idDestinatario;
	private int idVeiculoVinculado;
	private int idFrotaVinculada;
	private int idPedido;
	
	// CONSTRUTOR PADRAO
	public Pedido(){}
	
	public Pedido( int iDremetente, int iDdestinatario) {
		
		this.idRemetente = iDremetente;
		this.idDestinatario = iDdestinatario;
	}



	public java.sql.Date getDataEntrega() {
		return dataEntrega;
	}



	public void setDataEntrega(java.sql.Date dataEntrega) {
		this.dataEntrega = dataEntrega;
	}



	public String getStatus() {
		return status;
	}



	public void setStatus(String status) {
		this.status = status;
	}



	public String getJustificativaStatus() {
		return justificativaStatus;
	}



	public void setJustificativaStatus(String justificativaStatus) {
		this.justificativaStatus = justificativaStatus;
	}



	public float getComprimento() {
		return comprimento;
	}



	public void setComprimento(float comprimento) {
		this.comprimento = comprimento;
	}



	public float getAltura() {
		return altura;
	}



	public void setAltura(float altura) {
		this.altura = altura;
	}



	public float getLargura() {
		return largura;
	}



	public void setLargura(float largura) {
		this.largura = largura;
	}



	public float getPeso() {
		return peso;
	}



	public void setPeso(float peso) {
		this.peso = peso;
	}



	public int getPrazo() {
		return prazo;
	}


	public void setPrazo(int prazo) {
		this.prazo = prazo;
	}

	public java.sql.Date getDataCadastro() {
		return dataCadastro;
	}



	public void setDataCadastro(java.sql.Date data) {
		this.dataCadastro = data;
	}



	public int getIdRemetente() {
		return idRemetente;
	}



	public void setIdRemetente(int idRemetente) {
		this.idRemetente = idRemetente;
	}



	public int getIdDestinatario() {
		return idDestinatario;
	}



	public void setIdDestinatario(int idDestinatario) {
		this.idDestinatario = idDestinatario;
	}



	public int getIdVeiculoVinculado() {
		return idVeiculoVinculado;
	}



	public void setIdVeiculoVinculado(int idVeiculoVinculado) {
		this.idVeiculoVinculado = idVeiculoVinculado;
	}



	public int getIdFrotaVinculada() {
		return idFrotaVinculada;
	}



	public void setIdFrotaVinculada(int idFrotaVinculada) {
		this.idFrotaVinculada = idFrotaVinculada;
	}

	public int getIdPedido() {
		return idPedido;
	}

	public void setIdPedido(int idPedido) {
		this.idPedido = idPedido;
	}

	

}
