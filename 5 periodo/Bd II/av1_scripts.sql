

CREATE TABLE CLIENTE(
	IDCLIENTE	INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	NOMECLIENTE	VARCHAR(40),
	CPF			VARCHAR(11),
	SEXO		CHAR(1),
	CIDADE		VARCHAR(40),
	ESTADO		CHAR(2),
	ANOINGRESSO INT	
)


CREATE TABLE TIPOINVESTIMENTO(
	IDTIPOINVESTIMENTO	INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	DESCRICAO			VARCHAR(100),
	TAXAREMUNERACAO		NUMERIC(12,2)
)


CREATE TABLE INVESTIMENTO(
	IDINVESTIMENTO		INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	IDCLIENTE			INT NOT NULL,
	IDTIPOINVESTIMENTO	INT NOT NULL,
	VALOR				MONEY,
	DATADEPOSITO		DATE,	
	FOREIGN KEY (IDCLIENTE) REFERENCES CLIENTE,
	FOREIGN KEY (IDTIPOINVESTIMENTO) REFERENCES TIPOINVESTIMENTO
)


