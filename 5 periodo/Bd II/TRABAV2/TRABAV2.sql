BEGIN
	DECLARE @IDOBRA INT
	DECLARE @IDEXEMPLAR INT
	DECLARE @SITUACAO CHAR
	DECLARE @IDEX_EMPR_OBRA INT
	DECLARE @DTDEV DATE
	DECLARE @DATA_PREV_DEV DATE
	
