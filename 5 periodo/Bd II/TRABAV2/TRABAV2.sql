CREATE PROCEDURE SP_QUANTOS
	@IDALUNO INT, 
	@TOTAL INT OUTPUT
	
AS
SET @TOTAL = (SELECT COUNT(*)
		FROM EXEMPLAR AS EX
		INNER JOIN EMPRESTIMO_OBRA AS EO
			ON EX.ID_EXEMPLAR = EO.ID_EXEMPLAR
		INNER JOIN EMPRESTIMO AS EM
			ON EO.ID_EMPRESTIMO = EM.ID_EMPRESTIMO
